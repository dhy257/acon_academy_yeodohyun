
--CUSTOMER
CREATE TABLE YONGCUSTOMER(
    ID VARCHAR2(50) PRIMARY KEY, --아이디, 주키
    PW VARCHAR2(50) UNIQUE, --비번
    NAME VARCHAR2(50) NOT NULL, --이름
    GENDER CHAR(6) NULL,--성별
    BIRTHDAY DATE,--생일
    PHONE CHAR(13) CHECK ( PHONE  LIKE  '010-%-____') NOT NULL,--전화번호
    CONSTRAINT YONGCUSTOMER_ID_PW CHECK (ID != PW)--아이디와 비번은 달라야함
);

