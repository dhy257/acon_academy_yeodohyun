---------------------------영수증----------------------------
CREATE TABLE RECEIPT(
    RECEIPTNUM NUMBER PRIMARY KEY, --영수증 번호
    CUSTNAME VARCHAR2(50) REFERENCES YONGCUSTOMER(ID), --고객 이름
    MALLNAME VARCHAR2(50) REFERENCES ONLINEMALL(NAME) ON DELETE SET NULL, --몰 이름, 파산 시 NULL
    PRODUCT VARCHAR2(50) REFERENCES ONLINEMALL(PRODUCT), --제품 이름
    BUYDATE DATE NOT NULL, --구매 날짜
    NUM NUMBER(10) NOT NULL--구매개수
);

