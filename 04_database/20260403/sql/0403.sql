--CEIL  : 주어진 숫자에서 가까운 큰 정수   3.56  ->  4
--FLOOR : 주어진 숫자에서 가까운 작은 정수   3.56  ->  3
--                                      -3.46   ->  큰,  -3     작은거 -4


SELECT FLOOR( 3.56) FROM DUAL;   -- 3
SELECT CEIL( 3.56) FROM DUAL;   -- 4

SELECT FLOOR( -3.56) FROM DUAL;       -- -4
SELECT CEIL( -3.56) FROM DUAL;        -- -3


