--형변환
--문자 :   VARCHAR2  , CHAR
--숫자  :  NUMBER 
--날짜  : DATE
--1)형변환  - 표시형식 
-- 숫자  ->  문자  (천단위, 통화단위)
-- 날짜  ->  문자  (원하는 표시형식으로 지정하고 싶을 때)

--2) 숫자연산을 위해서
-- 문자  ->  숫자

--3) 날짜데이터로 다루기 위해서 
-- 문자  ->  날짜 

--형변환함수
--명시적형변환 , 묵시적형변환(자동형변환)
SELECT  2+'2' FROM DUAL;  --  2+ '2' -> 문자  2가 자동으로 숫자로 변환되었음  (자동형변환)
--
SELECT 2 + TO_NUMBER('2') FROM DUAL;

